import type {
	ChordQuality,
	FunctionalCategory,
	NoteName,
	ScaleMode,
} from "../types/music";
import { NOTE_NAMES, transposeNote } from "../lib/music/notes";

export interface StandardChord {
	root: NoteName;
	quality: ChordQuality;
	beats: number;
	fn: FunctionalCategory;
	degree: string;
}

export interface StandardSection {
	name: string;
	chords: StandardChord[];
}

export interface JazzStandard {
	id: string;
	title: string;
	composer: string;
	originalKey: NoteName;
	mode: "major" | "minor";
	style: string;
	bpm: number;
	difficulty: 1 | 2 | 3 | 4 | 5;
	sections: StandardSection[];
}

const T: FunctionalCategory = "tonic";
const S: FunctionalCategory = "subdominant";
const D: FunctionalCategory = "dominant";

function ch(
	root: NoteName,
	quality: ChordQuality,
	beats: number,
	fn: FunctionalCategory,
	degree: string,
): StandardChord {
	return { root, quality, beats, fn, degree };
}

export function getRecommendedScales(
	quality: ChordQuality,
	fn: FunctionalCategory,
): ScaleMode[] {
	switch (quality) {
		case "maj7":
		case "maj9":
		case "major":
			return fn === "tonic" ? ["ionian", "lydian"] : ["lydian"];
		case "maj7sharp11":
			return ["lydian"];
		case "min7":
		case "min9":
		case "min11":
			return fn === "subdominant"
				? ["dorian"]
				: fn === "tonic"
					? ["dorian", "aeolian"]
					: ["dorian"];
		case "minor":
			return ["dorian", "aeolian"];
		case "dom7":
		case "dom9":
		case "dom13":
			return fn === "dominant"
				? ["mixolydian", "bebopDominant"]
				: ["mixolydian", "lydianDominant"];
		case "dom7b9":
			return ["diminishedHW", "altered"];
		case "dom7sharp9":
			return ["altered", "diminishedHW"];
		case "dom7sharp11":
			return ["lydianDominant", "wholeTone"];
		case "min7b5":
			return ["locrian"];
		case "dim7":
			return ["diminishedWH"];
		case "dim":
			return ["diminishedWH"];
		case "aug":
		case "aug7":
			return ["wholeTone"];
		case "minMaj7":
			return ["melodicMinor", "harmonicMinor"];
		default:
			return ["ionian"];
	}
}

export function transposeStandard(
	standard: JazzStandard,
	targetKey: NoteName,
): JazzStandard {
	const fromIdx = NOTE_NAMES.indexOf(standard.originalKey);
	const toIdx = NOTE_NAMES.indexOf(targetKey);
	const shift = (toIdx - fromIdx + 12) % 12;
	if (shift === 0) return standard;
	return {
		...standard,
		originalKey: targetKey,
		sections: standard.sections.map((section) => ({
			...section,
			chords: section.chords.map((chord) => ({
				...chord,
				root: transposeNote(chord.root, shift),
			})),
		})),
	};
}

export function getStandardsByDifficulty(
	maxDifficulty: number,
): JazzStandard[] {
	return JAZZ_STANDARDS.filter((s) => s.difficulty <= maxDifficulty);
}

